package com.madcow.method;

public class Feb_18_Meth_01 {
	
	public void add(int intNum1, int intNum2) {
		
		int intSum = intNum1 + intNum2;
		System.out.println(intSum);
	}
	
	public void add(int intNum1, float fNum1) {
		
		float intSum = intNum1 + fNum1;
		System.out.println(intSum);
	}
	
	public void add(double dNum, float fNum) {
		
		double fSum = dNum + fNum;
		System.out.println(fSum);
	}

}
